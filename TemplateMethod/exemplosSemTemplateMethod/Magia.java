package exemplosSemTemplateMethod;

public class Magia {
	private int dano;
	private int minInt;
	private int custo;
	private String nome;
	
	public int getDano() {
		return dano;
	}
	public void setDano(int dano) {
		this.dano = dano;
	}
	public int getMinInt() {
		return minInt;
	}
	public void setMinInt(int minInt) {
		this.minInt = minInt;
	}
	public int getCusto() {
		return custo;
	}
	public void setCusto(int custo) {
		this.custo = custo;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
}
